EOF

echo -e "${GREEN}✓ Updated .firebaserc${NC}"
echo ""

echo "============================================"
echo -e "${GREEN}✓ FlutterFire Configuration Complete!${NC}"
echo "============================================"
echo ""
echo "Projects configured:"
echo -e "  User App:     ${GREEN}$USER_PROJECT_ID${NC}"
echo -e "  Provider App: ${GREEN}$PROVIDER_PROJECT_ID${NC}"
echo ""
echo "Next steps:"
echo "1. Add google-services.json files:"
echo "   - android/app/src/user/google-services.json"
echo "   - android/app/src/provider/google-services.json"
echo ""
echo "2. Run the apps:"
echo "   flutter run -t lib/main_user.dart --flavor user"
echo "   flutter run -t lib/main_provider.dart --flavor provider"
echo ""
